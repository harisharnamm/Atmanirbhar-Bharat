/**
 * Convert Hindi district name back to English for constituency lookup
 * @param hindiDistrict - The district name in Hindi
 * @returns The district name in English, or the original name if not found
 */
export function getEnglishDistrictFromHindi(hindiDistrict: string): string {
  // Create reverse mapping: Hindi -> English
  const hindiToEnglish: Record<string, string> = {}
  Object.entries(DISTRICT_HINDI_MAPPING).forEach(([english, hindi]) => {
    hindiToEnglish[hindi] = english
  })

  return hindiToEnglish[hindiDistrict] || hindiDistrict
}

