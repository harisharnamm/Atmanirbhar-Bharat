/**
 * Convert English district name to Hindi
 * @param englishDistrict - The district name in English
 * @returns The district name in Hindi (Devanagari script), or the original name if not found
 */
export function getDistrictInHindi(englishDistrict: string): string {
  return DISTRICT_HINDI_MAPPING[englishDistrict] || englishDistrict
}

/**
 * Convert English constituency name to Hindi (removes numbers and converts name)
 * @param englishConstituency - The constituency name with number (e.g., "1 Ajmer")
 * @returns The constituency name in Hindi (Devanagari script), or the cleaned name if not found
 */
export function getConstituencyInHindi(englishConstituency: string): string {
  // Remove the number prefix (e.g., "1 Ajmer" -> "Ajmer")
  let cleanName = englishConstituency.replace(/^\d+\s+/, '')
  // Remove suffixes like (ST), (SC), etc. (e.g., "Ajmer (ST)" -> "Ajmer")
  cleanName = cleanName.replace(/\s*\([^)]*\)\s*$/, '').trim()
  return CONSTITUENCY_HINDI_MAPPING[cleanName] || cleanName
}

